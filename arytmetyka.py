# przypisz do zmiennych x i y wartości liczbowe korzystając
# korzystając z print wypisz podstawowe działąnia arytmetyczne +, -, / , *, dzielenie całk, reszta z dziel, potęgowanie
# uruchom program kilka razy dla róznych wart x i y 
#
# automatyczne formatowanie  crl+ alt + l

x = 10
y = 3
print("x=", x, "y=", y)
print("Dodawanie: ", x + y)
print("Odejmowanie: ", x - y)
print("Mnożenie: ", x * y)
print("Dzielenie: ", x / y)
print("Dzielenie całkowite: ", x // y)
print("Reszta z dzielenia: ", x % y)
print("Potęgowanie: ", x ** y)
