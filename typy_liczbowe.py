# w Pythonie mamy:
# liczby całkowite - integer
print(type(999))

# zamiana na liczbę
print(int("10"))

print(int("10", base=10))

# liczba zmiennoprzecinkowa
print(float())
print(float(10/3))
## notacja naukowa
print(1e12)

# liczby zespolone complex
print(complex())
print(10+11j)

# wartości bool: True, False
print(bool())
print(10+True)
# zadanie domowe
print(not True)

